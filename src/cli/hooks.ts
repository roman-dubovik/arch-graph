// Git hook install/uninstall/status.
//
// Default mode: pre-commit — installs a shell block that validates the graph can
// be rebuilt before the commit. Generated graph artifacts are local build output
// and are intentionally not staged by the hook.
//
// Idempotent: re-running install replaces the block. If a hook already exists
// from another tool, our block is appended without disturbing it.
// Switching modes: the marker is stripped from the old file and written to the new.

import { existsSync } from 'node:fs';
import { chmod, mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';

import { atomicWrite } from './atomic-write.js';
import { appendBlock, replaceMarkedSection, stripMarkedSection } from './marker-block.js';
import { registerProject } from './project-registry.js';

// Shell-comment markers for files that are sourced or executed as shell.
// Used in git hooks and SessionStart.sh.
export const MARK_START = '# >>> arch-graph >>>';
export const MARK_END = '# <<< arch-graph <<<';

// Markdown/HTML-comment markers for files where '#' would render as a heading
// (.cursorrules is treated as markdown by Cursor; CLAUDE.md is markdown).
// Keep them visually distinct from the shell markers above so a quick `grep`
// over a repo can answer "did arch-graph touch this file as text or as shell?"
export const CURSOR_MARK_START = '<!-- arch-graph:cursor -->';
export const CURSOR_MARK_END = '<!-- /arch-graph:cursor -->';

export function preCommitHookPath(repo: string): string {
    return resolve(repo, '.git', 'hooks', 'pre-commit');
}

export function postCommitHookPath(repo: string): string {
    return resolve(repo, '.git', 'hooks', 'post-commit');
}

// ---------------------------------------------------------------------------
// Hook body templates
// ---------------------------------------------------------------------------

function buildPreCommitBody(includeSemantic: boolean): string {
    void includeSemantic;

    return `${MARK_START}
# Validate arch-graph before commits that touch TypeScript files.
# Managed by \`arch-graph hook install\` — edit between markers will be overwritten.
if command -v arch-graph >/dev/null 2>&1; then
    if git diff --cached --name-only --diff-filter=ACMR 2>/dev/null | grep -qE '\\.ts$'; then
        # Real build errors (config parse, I/O) block the commit. Recall-floor
        # regressions are advisory by default and do not block — see \`arch-graph
        # build --strict\` for CI gating.
        arch-graph build --quiet || exit 1
    fi
fi
${MARK_END}
`;
}

function buildPostCommitBody(includeSemantic: boolean): string {
    const semanticBlock = includeSemantic ? `
        # Incremental semantic index update (default-on).
        arch-graph semantic build --quiet || true` : '';

    return `${MARK_START}
# Auto-rebuild arch-graph after commits that touch TypeScript files.
# Generated by \`arch-graph hook install --mode=post-commit\` — edit between markers will be
# overwritten on the next install. Remove with \`arch-graph hook uninstall\`.
if command -v arch-graph >/dev/null 2>&1; then
    # \`git diff-tree -r --no-commit-id --name-only HEAD\` works on the initial
    # commit too (HEAD~1 would not), and prints one path per line.
    if git diff-tree --no-commit-id --name-only -r HEAD 2>/dev/null | grep -qE '\\.ts$'; then
        arch-graph build --quiet || true${semanticBlock}
    fi
fi
${MARK_END}
`;
}

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export type HookMode = 'pre-commit' | 'post-commit';

export interface HookArgs {
    repo: string; // path to repo root (we resolve .git/hooks/<hook> under it)
    mode: HookMode;
    /**
     * When true, the installed hook will NOT include the `arch-graph semantic build`
     * step — preserving the legacy structural-only hook behaviour.
     * Default: false (semantic build is included by default).
     */
    noIncludeSemantic?: boolean;
}

// ---------------------------------------------------------------------------
// Argument parsing
// ---------------------------------------------------------------------------

export function parseHookArgs(argv: string[]): { sub: string; args: HookArgs } {
    const [sub, ...rest] = argv;
    let repo = '.';
    let mode: HookMode = 'pre-commit';
    let noIncludeSemantic = false;

    for (let i = 0; i < rest.length; i++) {
        const a = rest[i]!;
        if (a === '--repo' && rest[i + 1]) {
            repo = rest[++i]!;
        } else if (a.startsWith('--repo=')) {
            repo = a.slice('--repo='.length);
        } else if (a === '--mode' && rest[i + 1]) {
            mode = validateMode(rest[++i]!);
        } else if (a.startsWith('--mode=')) {
            mode = validateMode(a.slice('--mode='.length));
        } else if (a === '--no-include-semantic') {
            noIncludeSemantic = true;
        }
    }
    return { sub: sub ?? '', args: { repo: resolve(repo), mode, noIncludeSemantic } };
}

function validateMode(raw: string): HookMode {
    if (raw === 'pre-commit' || raw === 'post-commit') return raw;
    process.stderr.write(`error: --mode must be "pre-commit" or "post-commit", got "${raw}"\n`);
    process.exit(2);
}

